#include "fillit.h"

void	ft_check_symbols(char const *i)
{
	while (*i)
	{
		if (*i != '#' && *i != '.' && *i != '\n')
		{
			ft_putstr("error\n");
			exit(1);
		}
		i++;
	}
}
